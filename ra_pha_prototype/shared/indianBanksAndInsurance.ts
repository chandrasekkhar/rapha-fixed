// Premium calculation helper
export function calculateTermInsurancePremium(
  age: number,
  coverageAmount: number,
  term: number,
  gender: "male" | "female" = "male"
): number {
  // Simplified premium calculation based on age, coverage, and term
  const baseRate = 0.005; // 0.5% of coverage
  const ageMultiplier = 1 + (age - 25) * 0.02; // 2% increase per year above 25
  const termMultiplier = 1 + (term - 10) * 0.05; // 5% increase per year above 10
  const genderMultiplier = gender === "female" ? 0.85 : 1; // 15% discount for females

  const monthlyPremium =
    (coverageAmount * baseRate * ageMultiplier * termMultiplier * genderMultiplier) / 12;
  return Math.round(monthlyPremium);
}

// Health insurance premium calculation
export function calculateHealthInsurancePremium(
  age: number,
  sumInsured: number,
  familySize: number = 1
): number {
  // Simplified premium calculation for health insurance
  const baseRate = 0.008; // 0.8% of sum insured
  const ageMultiplier = age < 30 ? 1 : age < 45 ? 1.25 : age < 60 ? 1.75 : 2.5;
  const familyMultiplier = 1 + (familySize - 1) * 0.4; // 40% increase per additional family member

  const annualPremium =
    sumInsured * baseRate * ageMultiplier * familyMultiplier;
  return Math.round(annualPremium);
}
